"""Завдання. Оптимізація виробництва

Компанія виробляє два види напоїв: "Лимонад" і "Фруктовий сік". Для виробництва цих напоїв використовуються різні інгредієнти 
та обмежена кількість обладнання. Задача полягає у максимізації виробництва, враховуючи обмежені ресурси.

Умови завдання:

"Лимонад" виготовляється з "Води", "Цукру" та "Лимонного соку".
"Фруктовий сік" виготовляється з "Фруктового пюре" та "Води".
Обмеження ресурсів: 100 од. "Води", 50 од. "Цукру", 30 од. "Лимонного соку" та 40 од. "Фруктового пюре".
Виробництво одиниці "Лимонаду" вимагає 2 од. "Води", 1 од. "Цукру" та 1 од. "Лимонного соку".
Виробництво одиниці "Фруктового соку" вимагає 2 од. "Фруктового пюре" та 1 од. "Води".
Використовуючи PuLP, створіть модель, яка визначає, скільки "Лимонаду" та "Фруктового соку" потрібно виробити для максимізації 
загальної кількості продуктів, дотримуючись обмежень на ресурси. Напишіть програму, код якої максимізує загальну кількість 
вироблених продуктів "Лимонад" та "Фруктовий сік", враховуючи обмеження на кількість ресурсів."""

import pulp

# Створюємо модель задачі максимізації виробництва
model = pulp.LpProblem("Maximize Production", pulp.LpMaximize)

# Визначаємо змінні для кількості "Лимонаду" та "Фруктового соку"
Lemonade = pulp.LpVariable('Lemonade', lowBound=0, cat='Continuous')  # Кількість "Лимонаду"
FruitJuice = pulp.LpVariable('FruitJuice', lowBound=0, cat='Continuous')  # Кількість "Фруктового соку"

# Додаємо функцію цілі: максимізуємо загальну кількість вироблених продуктів
model += Lemonade + FruitJuice, "Total Production"

# Встановлюємо обмеження на ресурси
model += 2 * Lemonade + FruitJuice <= 100, "Water Constraint"  # Обмеження на "Воду"
model += Lemonade <= 50, "Sugar Constraint"  # Обмеження на "Цукор"
model += Lemonade <= 30, "Lemon Juice Constraint"  # Обмеження на "Лимонний сік"
model += 2 * FruitJuice <= 40, "Fruit Puree Constraint"  # Обмеження на "Фруктове пюре"

# Розв'язуємо задачу
model.solve()

# Виводимо результати
print("Виробляти 'Лимонаду':", Lemonade.varValue)
print("Виробляти 'Фруктового соку':", FruitJuice.varValue)
print("Загальна кількість вироблених продуктів:", pulp.value(model.objective))

# Максимальна кількість лимонаду обмежена 30 одиницями лимонного соку, а фруктовий сік обмежений 20 одиницями фруктового пюре
